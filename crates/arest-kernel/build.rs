// crates/arest-kernel/build.rs
//
// Emits `$OUT_DIR/ui_assets.rs` with a static table of the ui.do
// static bundle (`apps/ui.do/dist/`). Each entry is
// `(http_path, &[u8])` where `http_path` is the leading-slash URL
// (e.g. `/index.html`, `/assets/index-DjXaD7eJ.js`) and the byte
// slice comes from `include_bytes!` pointed at an absolute path on
// the build machine.
//
// When `apps/ui.do/dist/` is missing (fresh clone, tests not run,
// Docker context that didn't copy the bundle), the table is empty.
// `src/assets.rs` falls through to `None` in that case and the
// handler keeps serving the legacy plaintext banner.

use std::env;
use std::fs;
use std::path::{Path, PathBuf};

fn main() {
    let manifest_dir = PathBuf::from(env::var("CARGO_MANIFEST_DIR").unwrap());
    let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());

    // Repo layout: crates/arest-kernel/ → ../../apps/ui.do/dist/
    let dist = manifest_dir
        .join("..").join("..")
        .join("apps").join("ui.do").join("dist");

    let mut entries: Vec<(String, PathBuf)> = Vec::new();
    if dist.is_dir() {
        collect(&dist, &dist, &mut entries);
        entries.sort_by(|a, b| a.0.cmp(&b.0));
    } else {
        println!(
            "cargo:warning=apps/ui.do/dist not found at {} — kernel will boot without the UI bundle",
            dist.display(),
        );
    }

    let mut out = String::new();
    out.push_str("// Generated by build.rs — do not edit.\n");
    out.push_str("pub static UI_ASSETS: &[(&str, &[u8])] = &[\n");
    for (http_path, fs_path) in &entries {
        // `include_bytes!` accepts absolute paths; forward slashes
        // work on both Windows and Linux, so we normalise here.
        let abs = fs_path
            .canonicalize()
            .unwrap_or_else(|_| fs_path.clone())
            .to_string_lossy()
            .replace('\\', "/");
        // Strip the Windows `\\?\` UNC prefix that `canonicalize` adds
        // — rustc's include_bytes! rejects it on Windows.
        let abs = abs.strip_prefix("//?/").unwrap_or(&abs).to_string();
        out.push_str(&format!(
            "    (\"{}\", include_bytes!(\"{}\")),\n",
            http_path, abs,
        ));
    }
    out.push_str("];\n");

    let out_path = out_dir.join("ui_assets.rs");
    fs::write(&out_path, out).expect("write ui_assets.rs");

    // Re-run whenever the bundle changes.
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed={}", dist.display());
    for (_, p) in &entries {
        println!("cargo:rerun-if-changed={}", p.display());
    }
}

fn collect(root: &Path, dir: &Path, out: &mut Vec<(String, PathBuf)>) {
    let entries = match fs::read_dir(dir) {
        Ok(e) => e,
        Err(_) => return,
    };
    for entry in entries.flatten() {
        let path = entry.path();
        if path.is_dir() {
            collect(root, &path, out);
        } else if path.is_file() {
            let rel = path
                .strip_prefix(root)
                .expect("path under root")
                .to_string_lossy()
                .replace('\\', "/");
            out.push((format!("/{}", rel), path));
        }
    }
}
